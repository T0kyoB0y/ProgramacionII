package Operadores_Aritmeticos;
 
public class Main {
	public static void main(String[] args) {

		int numero1, numero2, resultado1, resultado2, resultado3, resultado4;

		numero1 = 5;
		numero2 = 10;
		resultado1 = numero1 + numero2;
		resultado2 = numero1 - numero2;
		resultado3 = numero1 * numero2;
		resultado4 = numero1 / numero2;


		System.out.println("El resultado de la suma de " + numero1 + " y " + numero2 + " da como resultado: " + resultado1);
		System.out.println("El resultado de la resta de " + numero1 + " y " + numero2 + " da como resultado: " + resultado2);
		System.out.println("El resultado de la multiplicacion de " + numero1 + " y " + numero2 + " da como resultado: " + resultado3);
		System.out.println("El resultado de la divisionde " + numero1 + " y " + numero2 + " da como resultado: " + resultado4);

	}

}
